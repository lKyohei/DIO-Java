public class TiposVariaveis {
    public static void main(String[] args) {
       
       byte idade = 123;
       short ano = 2021;
       int cep = 21070333; // se começar com zero, talvez seja melhor usar outro tipo
       long cpf = 98765432109L; // se começar com zero, talvez seja melhor usar outro tipo e terminar com L.
       float pi = 3.14F; // tipo float tem que terminar em F
       double salarioMinimo = 2500.30;
        
       int numero = 5;
        
       System.out.println(numero);
       System.out.println(idade);
       System.out.println(ano);
       System.out.println(cep);
       System.out.println(cpf);
       System.out.println(pi);
       System.out.println(salarioMinimo);


       final double VALOR_DE_PI = 3.14;
       System.out.println(VALOR_DE_PI);
       
       
    }
}
